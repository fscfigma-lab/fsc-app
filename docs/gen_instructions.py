S = make_styles()

# ── Вспомогательные строительные блоки ────────────────────────────────────

def cover_header(role_name, role_desc, doc_type):
    """Шапка документа"""
    items = [
        Table([[
            Paragraph('<b>FSC</b>', ParagraphStyle('logo', fontName='Helvetica-Bold',
                      fontSize=28, textColor=C_AMBER, leading=32)),
            ''
        ]], colWidths=[2*cm, W-2*cm-3.6*cm],
            style=TableStyle([('VALIGN', (0,0), (-1,-1), 'MIDDLE')])),
        Spacer(1, 4*mm),
        HRFlowable(width='100%', thickness=2, color=C_AMBER, spaceAfter=6),
        Paragraph(f'Инструкция пользователя — {doc_type}', S['small']),
        Paragraph(f'<b>Роль: {role_name}</b>', S['h1']),
        Paragraph(role_desc, S['header_sub']),
        HRFlowable(width='100%', thickness=0.5, color=C_BORDER, spaceBefore=6, spaceAfter=12),
    ]
    return items

def section(title):
    return [Paragraph(title, S['h2'])]

def sub(title):
    return [Paragraph(title, S['h3'])]

def p(text):
    return Paragraph(text, S['body'])

def tip_box(text, color=C_AMBER2, border=C_AMBER, icon='💡'):
    t = Table([[Paragraph(f'<b>{icon}</b>', S['tip']),
                Paragraph(text, S['tip'])]],
              colWidths=[0.6*cm, W - 3.6*cm - 0.6*cm],
              style=TableStyle([
                  ('BACKGROUND', (0,0), (-1,-1), color),
                  ('BOX', (0,0), (-1,-1), 0.5, border),
                  ('LEFTPADDING', (0,0), (-1,-1), 5),
                  ('RIGHTPADDING', (0,0), (-1,-1), 5),
                  ('TOPPADDING', (0,0), (-1,-1), 4),
                  ('BOTTOMPADDING', (0,0), (-1,-1), 4),
                  ('VALIGN', (0,0), (-1,-1), 'TOP'),
                  ('ROUNDEDCORNERS', [3]),
              ]))
    return KeepTogether([t, Spacer(1, 3*mm)])

def warn_box(text):
    return tip_box(text, color=C_RED2, border=C_RED, icon='⚠')

def info_box(text):
    return tip_box(text, color=C_BLUE2, border=C_BLUE, icon='ℹ')

def color_legend(items):
    """Таблица с цветовыми обозначениями: [(hex, label, desc)]"""
    rows = []
    for (hex_c, label, desc) in items:
        rows.append([
            Table([['']], colWidths=[0.5*cm], rowHeights=[0.35*cm],
                  style=TableStyle([('BACKGROUND',(0,0),(-1,-1),HexColor(hex_c)),
                                    ('ROUNDEDCORNERS',[3])])),
            Paragraph(f'<b>{label}</b>', S['tip']),
            Paragraph(desc, S['tip']),
        ])
    t = Table(rows, colWidths=[0.7*cm, 3.5*cm, W-3.6*cm-4.2*cm-0.7*cm],
              style=TableStyle([
                  ('VALIGN', (0,0), (-1,-1), 'MIDDLE'),
                  ('ROWBACKGROUNDS', (0,0), (-1,-1), [C_LIGHT, white]),
                  ('TOPPADDING', (0,0), (-1,-1), 3),
                  ('BOTTOMPADDING', (0,0), (-1,-1), 3),
              ]))
    return KeepTogether([t, Spacer(1, 4*mm)])

def two_col(left_items, right_items):
    """Два столбца"""
    cw = (W - 3.6*cm - 8*mm) / 2
    def wrap(items):
        from reportlab.platypus import KeepInFrame
        return KeepInFrame(cw, 999*cm, items, mode='shrink')
    t = Table([[wrap(left_items), wrap(right_items)]],
              colWidths=[cw + 8*mm, cw],
              style=TableStyle([
                  ('VALIGN',(0,0),(-1,-1),'TOP'),
                  ('LEFTPADDING',(0,0),(-1,-1),0),
                  ('RIGHTPADDING',(0,0),(-1,-1),0),
                  ('TOPPADDING',(0,0),(-1,-1),0),
                  ('BOTTOMPADDING',(0,0),(-1,-1),0),
              ]))
    return [t, Spacer(1, 3*mm)]

def step_table(steps):
    """Нумерованные шаги: [(num, text)]"""
    rows = [[
        Paragraph(f'<b>{n}</b>', ParagraphStyle('n', fontName='Helvetica-Bold',
                  fontSize=12, textColor=white, alignment=TA_CENTER, leading=14)),
        Paragraph(text, S['body'])
    ] for n, text in steps]
    t = Table(rows, colWidths=[0.8*cm, W-3.6*cm-0.8*cm],
              style=TableStyle([
                  ('BACKGROUND', (0,0), (0,-1), C_AMBER),
                  ('VALIGN', (0,0), (-1,-1), 'TOP'),
                  ('TOPPADDING', (0,0), (-1,-1), 4),
                  ('BOTTOMPADDING', (0,0), (-1,-1), 4),
                  ('LEFTPADDING', (0,0), (-1,-1), 4),
                  ('RIGHTPADDING', (0,0), (-1,-1), 4),
                  ('LINEBEFORE', (1,0), (1,-1), 0.5, C_BORDER),
                  ('ROWBACKGROUNDS', (1,0), (1,-1), [C_LIGHT, white]),
              ]))
    return KeepTogether([t, Spacer(1, 4*mm)])

def nav_table(rows_data):
    """Таблица навигации: [(icon, name, desc)]"""
    rows = [[
        Paragraph(icon, ParagraphStyle('ic', fontName='Helvetica', fontSize=14,
                  alignment=TA_CENTER, leading=16)),
        Paragraph(f'<b>{name}</b>', S['tip']),
        Paragraph(desc, S['tip']),
    ] for icon, name, desc in rows_data]
    t = Table(rows, colWidths=[0.9*cm, 3.2*cm, W-3.6*cm-4.1*cm],
              style=TableStyle([
                  ('VALIGN', (0,0), (-1,-1), 'MIDDLE'),
                  ('ROWBACKGROUNDS', (0,0), (-1,-1), [C_LIGHT, white]),
                  ('TOPPADDING', (0,0), (-1,-1), 5),
                  ('BOTTOMPADDING', (0,0), (-1,-1), 5),
                  ('LEFTPADDING', (0,0), (-1,-1), 5),
                  ('BOX', (0,0), (-1,-1), 0.5, C_BORDER),
                  ('LINEBELOW', (0,0), (-1,-2), 0.3, C_BORDER),
              ]))
    return KeepTogether([t, Spacer(1, 4*mm)])

def footer(role, doc):
    return Paragraph(f'FSC App · {doc} · {role} · fscfigma-lab.github.io/fsc-app',
                     S['center'])

def build_pdf(filename, elements):
    path = os.path.join(OUT, filename)
    doc = SimpleDocTemplate(path, pagesize=A4,
                            leftMargin=1.8*cm, rightMargin=1.8*cm,
                            topMargin=1.6*cm, bottomMargin=1.6*cm)
    doc.build(elements)
    print(f'  ✓ {filename}')
    return path

